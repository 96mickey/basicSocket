var io = require("socket.io").listen(app.listen(5000));

io.sockets.on("connection", function(socket) {
  socket.emit("Start_Chat");

  socket.on("hitting", function(data) {
    io.sockets.emit("hitting_event", { key: data.key });
  });
});

